let date = new Date();
let time = date.getHours();

function timeOfDay() {
    if (time >= 0 && time < 6) return imagesNight;
    if (time >= 6 && time < 12) return imagesMorning;
    if (time >= 12 && time < 18) return imagesAfternoon;
    if (time >= 18 ) return imagesEvening
};

